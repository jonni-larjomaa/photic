Route::filter('auth', function()
{
	if (!Auth::check())
        {
            return Redirect::to('/login');
        }
});

Route::filter('csrf', function()
{
	if (Session::token() != Input::get('_token'))
	{
		throw new Illuminate\Session\TokenMismatchException;
	}
});
